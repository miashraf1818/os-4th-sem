public class constructor 
{
    int a;
    String s;
    boolean b;
    float f;

    public static void main(String[] args) 
    {
        constructor t=new constructor();
        System.out.println(t.a);
        System.out.println(t.s);
        System.out.println(t.b);
        System.out.println(t.f);
    }
}
